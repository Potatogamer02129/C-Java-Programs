def parse_seq(fasta_string):
    sequences = []
    current_seq = ''
    for line in fasta_string.strip().splitlines():
        line = line.strip()
        if line.startswith('>'):
            if current_seq:
                sequences.append(current_seq)
                current_seq=''
        else:
            current_seq+=line
    if current_seq:
        sequences.append(current_seq)
    return sequences

